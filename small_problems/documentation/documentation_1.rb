# Class and Instance Methods

# Locate the ruby documentation for methods File::path and File#path. How are they different?

## Answer ##
# > File::path is an example of a class method, while File#path is an example of an instance method
# > File::path is called on the File class, uses a file path designation as an argument, 
# and outputs the file path as a string.
# > File#path is used on a specific instance of the File class,
#  and outputs its file path as a string. There are some additional limitations that don't apply to File::path
